/**
 * Centralized internationalization dictionary.
 *
 * To add a language: add its code to `UiLanguage` in `@/types`, add a
 * `LANGUAGES` entry, and add a `STRINGS` map. Missing keys fall back to English,
 * so a partial translation is fine — translate incrementally.
 */
import type { UiLanguage } from '@/types'

